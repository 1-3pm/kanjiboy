$(function(){
		$('#question').click(function(){
			var iteration=$(this).data('iteration')||1
			switch ( iteration) {
				case 1:

        var frameValue = $( "input" ).val();
        var $random = (Math.floor(Math.random() * frameValue + 1));

            var $randomKeyword = $keyword[$random];
            var $randomKanji = $kanji[$random];

            $("#number").html($random);

            $("#number").css({"display": "none"});

            $("#showquestion").html($randomKeyword);

            $("#showanswer").html($randomKanji);

            $("#showanswer").css({"display": "none"});

					break;

				case 2:

        $("#showanswer").css({"display": "inline"});

        $("#number").css({"display": "inline"});

					break;
			}
			iteration++;
			if (iteration>2) iteration=1
			$(this).data('iteration',iteration)
		})
	})

